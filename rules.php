        <h2>القوانين والإعلانات - <?= htmlspecialchars($server_name) ?></h2>
        <?php if ($message): ?>
        <p style="color:#0f0;"><?= $message ?></p>
        <?php endif; ?>
        <form method="POST">
            <h3>الإعلانات</h3>
            <input type="text" name="hostname" placeholder="اسم الخادم">
            <input type="text" name="gamemode" placeholder="الجيم مود" value="default">
            <textarea name="custom_rule" placeholder="قاعدة مخصصة..."></textarea>
            <button type="submit">حفظ</button>
        </form>
