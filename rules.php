<?php
require_once __DIR__ . '/config.php';
if (!isset($_SESSION['logged_in'])) {
    header('Location: index.php');
    exit;
}

$server_name = $_GET['server'] ?? $config['default_server'];
$server_dir = __DIR__ . "/servers/{$server_name}";
$message = '';

function get_server_config($server_dir) {
    $cfg_file = "{$server_dir}/server.cfg";
    if (!file_exists($cfg_file)) return [];
    return file($cfg_file, FILE_IGNORE_NEW_LINES | FILE_SKIP_EMPTY_LINES);
}

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $rules = [
        'No cheating',
        'Respect all players', 
        $_POST['custom_rule'] ?? ''
    ];
    
    $rules_content = "# Server Rules\n# Generated by SA-MP Panel\n\n";
    $rules_content .= "hostname " . ($_POST['hostname'] ?? '') . "\n";
    $rules_content .= "gamemode0 " . ($_POST['gamemode'] ?? 'default') . " 1\n\n";
    $rules_content .= "rules\n";
    foreach (array_filter($rules) as $rule) {
        $rules_content .= "{$rule}\n";
    }
    
    file_put_contents("{$server_dir}/rules.cfg", $rules_content);
    $message = "تم حفظ القوانين";
}
